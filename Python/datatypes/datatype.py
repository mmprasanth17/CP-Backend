#Integer

mobileNo = 1236547890
print('integer datatype: ',mobileNo,type(mobileNo))


temperature = 12.5
print('integer datatype: ',temperature,type(temperature))


complexNumber = 89j+60
print('integer datatype: ',complexNumber,type(complexNumber))

#sequence datatype

cource ='python'
print('String datatype: ',cource,type(cource))


cource =('python','java','php')
print('String datatype: ',cource,type(cource))

cource =['python','java','php']
print('String datatype: ',cource,type(cource))


#Boolean

is_pass=True
print('boolean datatype: ',is_pass,type(is_pass))


#set

cource ={'python','java','php'}
print('String datatype: ',cource,type(cource))



