# membership operators

# -> in (True) if value is found in the sequence
# -> not in (True) if value is not found in the sequence
# -> it is used to test wheather a value or variable is in a sequence

course=['python','java','mysql','react','angular']
print('java' in course)  #True

print('HTML' in course)  #False

print('HTML' not in course)  #True

print('python' not in course)  #False
