def addition(value_1,value_2):
    Ans=value_1+value_2
    return Ans

def subtraction(value_1,value_2):
    Ans=value_1-value_2
    return Ans

def multiplication(value_1,value_2):
    Ans=value_1*value_2
    return Ans

def division(value_1,value_2):
    Ans=value_1/value_2
    return Ans